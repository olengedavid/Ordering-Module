<script>
import { Link } from '@inertiajs/vue3';

export default {
  name: 'AdminNavbar',
  components: {
    Link
  },
  data() {
    return {
      isDropdownOpen: false,
      isMobileMenuOpen: false,
      // Store the handler as a property so we can reference it when removing
      clickOutsideHandler: null
    }
  },
  methods: {
    toggleDropdown() {
      this.isDropdownOpen = !this.isDropdownOpen
    },
    toggleMobileMenu() {
      this.isMobileMenuOpen = !this.isMobileMenuOpen;
      
      // Prevent body scrolling when mobile menu is open
      if (this.isMobileMenuOpen) {
        document.body.style.overflow = 'hidden';
      } else {
        document.body.style.overflow = '';
      }
    },
    closeMobileMenu() {
      // Close mobile menu when a link is clicked
      this.isMobileMenuOpen = false;
      document.body.style.overflow = '';
    }
  },
  mounted() {
    // Define the handler function and store a reference to it
    this.clickOutsideHandler = (e) => {
      if (this.$refs.dropdown && !this.$refs.dropdown.contains(e.target)) {
        this.isDropdownOpen = false
      }
    }
    
    // Add the event listener using the stored reference
    document.addEventListener('click', this.clickOutsideHandler)
  },
  beforeUnmount() {
    // Remove the event listener using the stored reference
    if (this.clickOutsideHandler) {
      document.removeEventListener('click', this.clickOutsideHandler)
    }
  }
}
</script>

